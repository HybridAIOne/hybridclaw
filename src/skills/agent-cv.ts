const GENERATED_CV_MARKER = '<!-- Generated by HybridClaw agent CV. -->';
const AGENT_CV_STATE_SCHEMA_VERSION = 1;
const CV_STATE_FILE = '.CV.state.json';
const MAX_NARRATION_SOURCE_CHARS = 1_200;
const DEFAULT_CV_NARRATION_MAX_TOKENS = 1_200;
const DEFAULT_CV_NARRATION_TIMEOUT_MS = 20_000;
const APPROX_CHARS_PER_TOKEN = 4;

interface AgentCvEntry {
  run_id: string;
  session_id: string;
  skill_id: string;
  date: string;
  created_at: string;
  outcome: SkillRunEvent['outcome'];
  title: string;
  description: string;
}

interface AgentCvState {
  schema_version: typeof AGENT_CV_STATE_SCHEMA_VERSION;
  agent_id: string;
  updated_at: string;
  last_rendered_at: string | null;
  narration_cost_usd_by_day: Record<string, number>;
  entries: AgentCvEntry[];
}

interface CvNarration {
  run_id: string;
  title: string;
  description: string;
}

const pendingAgentCvRefreshes = new Map<string, NodeJS.Timeout>();
const pendingAgentCvRenderRefreshes = new Map<string, NodeJS.Timeout>();
